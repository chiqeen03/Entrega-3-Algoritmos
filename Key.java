public class Key {

    private int e;
    private int n;

    public Key(int e, int n) {
        this.e = e;
        this.n = n;
    }

    public int getE() {
        return e;
    }
    public void setE(int e) {
        this.e = e;
    }

    public int getN() {
        return n;
    }
    public void setN(int n) {
        this.n = n;
    }
}
